namespace Pulchritudinous\Queue\Config\Worker;

class Reader
    extends \Magento\Framework\Config\Reader\Filesystem
{
    protected $_idAttributes = [
        '/config/queue/worker' => 'id',
    ];

    public function __construct(
        \Magento\Framework\Config\FileResolverInterface $fileResolver,
        Converter $converter,
        SchemaLocator $schemaLocator,
        \Magento\Framework\Config\ValidationStateInterface $validationState,
        $fileName = 'worker.xml',
        $idAttributes = [],
        $domDocumentClass = 'Magento\Framework\Config\Dom',
        $defaultScope = 'global'
    ) {
        parent::__construct(
            $fileResolver,
            $converter,
            $schemaLocator,
            $validationState,
            $fileName,
            $idAttributes,
            $domDocumentClass,
            $defaultScope
        );
    }

    /**
     * Return newly created instance of a config merger
     *
     * @param string $initialContents
     *
     * @return \Magento\Framework\Config\Dom
     *
     * @throws \UnexpectedValueException
     */
    public function validateWorderConfig(string $xml) : \Magento\Framework\Config\Dom
    {
        $mergerClass = $this->_domDocumentClass;

        $result = new $mergerClass(
            $xml,
            $this->validationState,
            $this->_idAttributes,
            null,
            $this->_perFileSchema
        );

        if (!$result instanceof \Magento\Framework\Config\Dom) {
            throw new \UnexpectedValueException(
                "Instance of the DOM config merger is expected, got {$mergerClass} instead."
            );
        }

        return $result;
    }
}

